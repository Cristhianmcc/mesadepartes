    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        tramite = new Entidad.TramitesC();
        String nombre = txtNombret.getText();
        try {
            int editar = tg.eliminaTramites(nombre);
            if (editar == 1) {
                JOptionPane.showMessageDialog(null, "Oficina eliminado correctamente");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Oficina no eliminado correctamente");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No se pudo eliminar" + ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

