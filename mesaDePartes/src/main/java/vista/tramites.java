    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        tramite = new TramitesC();

        tramite.setNombre_tramite(txtNombret.getText());
        tramite.setDescripcion(txtDescriocion.getText());
        tramite.setCosto(txtCosto.getText());
        tramite.setRequisitos(txtRequisitos.getText());

        tramite.setDuracion_Estimada(txtDuracion.getText());
        tramite.setFecha_Actualizacion(txtFecha.getText());

        int swe = tg.agregaTramites(tramite);
        if (swe == 1) {
            JOptionPane.showMessageDialog(null, "Registro exitoso");
            limpiar();

            Entidad.TramitesC frm = new Entidad.TramitesC();
            frm.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Registro no exitoso");

        }


    }//GEN-LAST:event_btnAgregarActionPerformed

